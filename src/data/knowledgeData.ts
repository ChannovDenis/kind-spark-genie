// Helper functions
export function getDocumentsByCategory(categoryId: string): KnowledgeDocument[] {
  return knowledgeDocuments.filter(doc => doc.categoryId === categoryId);
}

export function getFAQsByCategory(categoryId: string): FAQItem[] {
  return faqItems.filter(faq => faq.categoryId === categoryId);
}

export function searchDocuments(query: string): KnowledgeDocument[] {
  const lowerQuery = query.toLowerCase();
  return knowledgeDocuments.filter(doc => 
    doc.title.toLowerCase().includes(lowerQuery) ||
    doc.content.toLowerCase().includes(lowerQuery) ||
    doc.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
}

export function searchFAQs(query: string): FAQItem[] {
  const lowerQuery = query.toLowerCase();
  return faqItems.filter(faq =>
    faq.question.toLowerCase().includes(lowerQuery) ||
    faq.answer.toLowerCase().includes(lowerQuery) ||
    faq.alternativeQuestions.some(q => q.toLowerCase().includes(lowerQuery))
  );
}
